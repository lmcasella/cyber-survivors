export class InputManager {
    constructor() {
        this.keys = {};
    }

    init() {
        window.addEventListener('keydown', (e) => this.onKeyDown(e));
        window.addEventListener('keyup', (e) => this.onKeyUp(e));
    }

    onKeyDown(e) {
        this.keys[e.code] = true;
    }

    onKeyUp(e) {
        this.keys[e.code] = false;
    }

    isKeyDown(keyCode) {
        return !!this.keys[keyCode];
    }
}